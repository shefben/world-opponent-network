///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
bool LobbyClient::IsMuted(bool checkGame)
{
	if(mClientFlags & RoutingClientFlag_IsMuted)
		return true;

	if(checkGame)
		return mGameFlags&RoutingMemberFlag_IsMuted?true:false;
	else
		return mChatFlags&RoutingMemberFlag_IsMuted?true:false;
}

