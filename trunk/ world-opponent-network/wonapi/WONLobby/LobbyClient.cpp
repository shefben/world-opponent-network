///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
bool LobbyClient::SameTeam(LobbyClient *theClient)
{
	if(theClient==NULL)
		return false;
	else if(mPlayer==NULL || theClient->mPlayer==NULL)
		return false;
	else
		return mPlayer->SameTeam(theClient->mPlayer);
}

