#include "LobbyCompletion.h"

using namespace WONAPI;

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
void LobbyCompletionBase::Complete(AsyncOpPtr theOp)
{
	if(Thread::GetCurThreadId()==WindowManager::GetThreadId())
		DoComplete(theOp);
	else
	{
		WindowManager *aMgr = WindowManager::GetDefaultWindowManager();
		if(aMgr!=NULL)
			aMgr->PostEvent(new LobbyCompletionEvent(theOp,this));
	}	
}

