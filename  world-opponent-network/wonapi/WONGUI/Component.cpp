void Component::Enable(bool isEnabled)
{
	if(isEnabled==!Disabled())
		return;

	SetComponentFlags(ComponentFlag_Disabled,!isEnabled);	
	EnableHook(isEnabled);

	if(mParent!=NULL)
		mParent->ChildChanged(this);

	Invalidate();
}

