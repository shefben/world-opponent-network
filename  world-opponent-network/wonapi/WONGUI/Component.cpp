void Component::SetSize(int width, int height)
{
	if(width<0)
		width = 0;
	if(height<0)
		height = 0;

	if(width==mBounds.Width() && height==mBounds.Height())
		return;

	mBounds.width = width;
	mBounds.height = height;
	
	SizeChanged();
}

